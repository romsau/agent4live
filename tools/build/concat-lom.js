#!/usr/bin/env node
'use strict';

// Concatenate the per-domain source files in app/lom_router/ into the single
// app/lom_router.js that Max [js] loads. Files are concatenated in
// alphabetical order — that's why they're prefixed `00_`, `10_`, `20_`, etc.,
// so helpers (00_) are defined before any caller in the concat output.
//
// Called by:
//   - tools/build/build.js  : at build time, before bundling for distribution.
//   - tools/dev-server/server.js : in watch mode, on every save in app/lom_router/.
//
// The generated app/lom_router.js IS committed to the repo so a fresh clone
// can drag the dev .amxd into Live without first running npm scripts.

const fs = require('fs');
const path = require('path');

const REPO = path.join(__dirname, '..', '..');
const SRC_DIR = path.join(REPO, 'app', 'lom_router');
const OUT_FILE = path.join(REPO, 'app', 'lom_router.js');

const HEADER = [
  '// AUTO-GENERATED — do not edit this file directly.',
  '// Source files live in app/lom_router/ and are concatenated by',
  '// tools/build/concat-lom.js. Edit there ; this file is regenerated',
  '// by `npm run build` and on every save under app/lom_router/ when',
  '// the dev server is running.',
  '',
  '// Runs in Max [js] engine — NOT Node.js',
  '// LiveAPI is globally available here',
  '',
  'inlets = 1;',
  'outlets = 1;',
  '',
  '',
].join('\n');

/**
 * Read every `*.js` in `app/lom_router/`, concatenate them in alphabetical
 * order under a fresh AUTO-GENERATED header, and write the result to
 * `app/lom_router.js`. Idempotent — safe to run on every save.
 */
function concat() {
  const files = fs
    .readdirSync(SRC_DIR)
    .filter((name) => name.endsWith('.js') && !name.endsWith('.test.js'))
    .sort();

  const parts = [HEADER];
  for (const file of files) {
    let content = fs.readFileSync(path.join(SRC_DIR, file), 'utf8');
    // Strip CJS export block — Max [js] is SpiderMonkey ES5 and chokes on
    // `module.exports = { foo, bar }` shorthand. The block exists only so
    // Jest can `require()` each file individually ; the concatenated bundle
    // exposes everything via top-level `function` declarations anyway.
    content = content.replace(/\n*\/\/ CJS export[\s\S]*$/, '\n');
    parts.push(`// ━━━ from ${file} ━━━`);
    parts.push('');
    parts.push(content.replace(/\n+$/, ''));
    parts.push('');
  }
  fs.writeFileSync(OUT_FILE, parts.join('\n').replace(/\n+$/, '') + '\n');
}

/* istanbul ignore if -- CLI entry guard, exercised end-to-end by the
   "concat-lom CLI smoke" test which spawns `node` ; sub-process coverage
   isn't propagated back to the parent Istanbul instrumenter. */
if (require.main === module) {
  concat();
  console.log('lom_router.js regenerated');
}

module.exports = { concat };
